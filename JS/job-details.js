// Get job ID from URL
function getJobIdFromURL() {
    const urlParams = new URLSearchParams(window.location.search);
    let jobId = urlParams.get('id');
    
    // If no ID in query string, check the URL path for SEO-friendly format
    if (!jobId) {
        const path = window.location.pathname;
        const match = path.match(/-(\d+)\.html/);
        if (match) {
            jobId = match[1];
        }
    }
    
    return jobId ? parseInt(jobId) : null;
}

// Create SEO-friendly URL
function updatePageURL(jobId, jobTitle) {
    // Create a URL-friendly version of the job title
    const cleanTitle = jobTitle.toLowerCase()
        .replace(/[^a-z0-9]+/g, '-')
        .replace(/(^-|-$)/g, '');
    
    // Create the new URL
    const newURL = `job-${jobId}-${cleanTitle}.html`;
    
    // Update the URL without reloading the page
    window.history.replaceState({}, '', newURL);
    
    // Update the page title for SEO
    document.title = `${jobTitle} | SarkariOfficer`;
    
    // Update meta description for SEO
    const job = jobDatabase[jobId];
    if (job) {
        const metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription) {
            metaDescription.setAttribute('content', 
                `Apply for ${job.title} in ${job.organization}. Qualification: ${job.qualification}. Last Date: ${job.lastDate}. Total Posts: ${job.totalPosts}`);
        }
    }
}

